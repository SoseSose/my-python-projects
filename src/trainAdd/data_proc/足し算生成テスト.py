開始トークン = "<"
終了トークン = ">"

def test_二項n桁足し算文字列生成_基本機能():
    生成器 = 足し算生成器()
    パラメータ = 二項足し算パラメータ(1, 1, 1.0, 1.0)
    結果 = 生成器.二項n桁足し算文字列生成(パラメータ)
    行リスト = 結果.strip().split("\n")

    assert len(行リスト) > 0
    for 行 in 行リスト:
        問題, 答え = 行.split("=")
        数値1, 数値2 = map(int, 問題.split("+"))
        答え数値 = int(答え.replace(開始トークン, "").replace(終了トークン, ""))

        assert 0 <= 数値1 <= 9
        assert 0 <= 数値2 <= 9
        assert 数値1 + 数値2 == 答え数値

