def test_二項n桁足し算文字列生成_交換法則():
    生成器 = 足し算生成器()
    パラメータ = 二項足し算パラメータ(1, 1, 1.0, 1.0)
    結果 = 生成器.二項n桁足し算文字列生成(パラメータ)
    行リスト = 結果.strip().split("\n")

    for i in range(0, len(行リスト), 2):
        if i + 1 >= len(行リスト):
            break

        問題1, 答え1 = 行リスト[i].split("=")
        問題2, 答え2 = 行リスト[i + 1].split("=")

        数値1_1, 数値1_2 = map(int, 問題1.split("+"))
        数値2_1, 数値2_2 = map(int, 問題2.split("+"))

        assert 数値1_1 == 数値2_2
        assert 数値1_2 == 数値2_1
        assert 答え1 == 答え2

