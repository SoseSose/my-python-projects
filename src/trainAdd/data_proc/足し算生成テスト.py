        ファイルパス = テストディレクトリ / f"{パラメータ.桁数1}桁{パラメータ.桁数2}桁.txt"
        assert ファイルパス.exists()

        with open(ファイルパス, "r") as f:
            内容 = f.read()
            assert len(内容) > 0

